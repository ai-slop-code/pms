func (s *Store) OpenFinanceMonth(ctx context.Context, propertyID int64, month string, openedBy *int64, loc *time.Location) (int, error) {
	year, m, err := parseMonthYYYYMM(month)
	if err != nil {
		return 0, err
	}
	// Anchor generated monthly transactions at UTC noon to keep the YYYY-MM
	// stable when filtering by stored timestamp prefix.
	monthAnchorUTC := time.Date(year, time.Month(m), 1, 12, 0, 0, 0, time.UTC)
	monthRef := fmt.Sprintf("%04d-%02d", year, m)
	monthStartRFC := monthAnchorUTC.Format(time.RFC3339)

	cleaningSummary, _ := s.ComputeCleaningMonthlySummary(ctx, propertyID, year, m, loc)

	tx, err := s.DB.BeginTx(ctx, nil)
	if err != nil {
		return 0, err
	}
	defer tx.Rollback()

	now := time.Now().UTC().Format(time.RFC3339)
	var by interface{}
	if openedBy != nil {
		by = *openedBy
	}
	if _, err := tx.ExecContext(ctx, `
		INSERT INTO finance_month_states (property_id, month, opened_at, opened_by)
		VALUES (?, ?, ?, ?)
		ON CONFLICT(property_id, month) DO NOTHING`, propertyID, monthRef, now, by); err != nil {
		return 0, err
	}

	rows, err := tx.QueryContext(ctx, `
		SELECT id, title, category_id, amount_cents, direction
		FROM finance_recurring_rules
		WHERE property_id = ?
		  AND active = 1
		  AND start_month <= ?
		  AND (end_month IS NULL OR end_month = '' OR end_month >= ?)
		  AND substr(effective_from, 1, 7) <= ?
		  AND (effective_to IS NULL OR substr(effective_to, 1, 7) >= ?)`,
		propertyID, monthRef, monthRef, monthRef, monthRef)
	if err != nil {
		return 0, err
	}
	defer rows.Close()
	inserted := 0
	for rows.Next() {
		var ruleID int64
		var title, direction string
		var categoryID sql.NullInt64
		var amount int
		if err := rows.Scan(&ruleID, &title, &categoryID, &amount, &direction); err != nil {
			return 0, err
		}
		ruleRef := fmt.Sprintf("%d:%s", ruleID, monthRef)
		var existing int64
		err := tx.QueryRowContext(ctx, `
			SELECT id
			FROM finance_transactions
			WHERE property_id = ?
			  AND source_type = 'recurring_rule'
			  AND source_reference_id = ?
			LIMIT 1`, propertyID, ruleRef).Scan(&existing)
		if err != nil && err != sql.ErrNoRows {
			return 0, err
		}
		if err == nil {
			_, err = tx.ExecContext(ctx, `
				UPDATE finance_transactions
				SET transaction_date = ?, direction = ?, amount_cents = ?, category_id = ?, note = ?, updated_at = ?
				WHERE id = ?`,
				monthStartRFC, direction, amount, nullInt64Value(categoryID), fmt.Sprintf("Recurring: %s (%s)", title, monthRef), now, existing)
			if err != nil {
				return 0, err
			}
			continue
		}
		// Compatibility path for early format where source_reference_id held only rule ID.
		var legacyID int64
		err = tx.QueryRowContext(ctx, `
			SELECT id
			FROM finance_transactions
			WHERE property_id = ?
			  AND source_type = 'recurring_rule'
			  AND source_reference_id = ?
			  AND note LIKE ?
			LIMIT 1`, propertyID, fmt.Sprintf("%d", ruleID), "%("+monthRef+")%").Scan(&legacyID)
		if err != nil && err != sql.ErrNoRows {
			return 0, err
		}
		if err == nil {
			_, err = tx.ExecContext(ctx, `
				UPDATE finance_transactions
				SET source_reference_id = ?, transaction_date = ?, direction = ?, amount_cents = ?, category_id = ?, note = ?, updated_at = ?
				WHERE id = ?`,
				ruleRef, monthStartRFC, direction, amount, nullInt64Value(categoryID), fmt.Sprintf("Recurring: %s (%s)", title, monthRef), now, legacyID)
			if err != nil {
				return 0, err
			}
			continue
		}
		res, err := tx.ExecContext(ctx, `
			INSERT INTO finance_transactions (
				property_id, transaction_date, direction, amount_cents, category_id, note,
				source_type, source_reference_id, is_auto_generated, created_at, updated_at
			) VALUES (?, ?, ?, ?, ?, ?, 'recurring_rule', ?, 1, ?, ?)`,
			propertyID, monthStartRFC, direction, amount, nullInt64Value(categoryID),
			fmt.Sprintf("Recurring: %s (%s)", title, monthRef), ruleRef, now, now)
		if err != nil {
			return 0, err
		}
		aff, _ := res.RowsAffected()
		inserted += int(aff)
	}
	if err := rows.Err(); err != nil {
		return 0, err
	}

	cleaningCategoryID, err := findFinanceCategoryByCodeTx(ctx, tx, propertyID, "cleaning_salary")
	if err != nil {
		return 0, err
	}
	if cleaningSummary != nil && cleaningSummary.FinalSalaryCents > 0 && cleaningCategoryID > 0 {
		var existingID int64
		err := tx.QueryRowContext(ctx, `
			SELECT id
			FROM finance_transactions
			WHERE property_id = ?
			  AND source_type = 'cleaning_salary'
			  AND source_reference_id = ?
			LIMIT 1`, propertyID, monthRef).Scan(&existingID)
		if err != nil && err != sql.ErrNoRows {
			return 0, err
		}
		if err == sql.ErrNoRows {
			_, err = tx.ExecContext(ctx, `
				INSERT INTO finance_transactions (
					property_id, transaction_date, direction, amount_cents, category_id, note,
					source_type, source_reference_id, is_auto_generated, created_at, updated_at
				) VALUES (?, ?, 'outgoing', ?, ?, ?, 'cleaning_salary', ?, 1, ?, ?)`,
				propertyID, monthStartRFC, cleaningSummary.FinalSalaryCents, cleaningCategoryID,
				fmt.Sprintf("Cleaner salary for %s", monthRef), monthRef, now, now)
		} else {
			_, err = tx.ExecContext(ctx, `
				UPDATE finance_transactions
				SET transaction_date = ?, amount_cents = ?, category_id = ?, note = ?, updated_at = ?
				WHERE id = ?`,
				monthStartRFC, cleaningSummary.FinalSalaryCents, cleaningCategoryID, fmt.Sprintf("Cleaner salary for %s", monthRef), now, existingID)
		}
		if err != nil {
			return 0, err
		}
	}
	if err := tx.Commit(); err != nil {
		return 0, err
	}
	return inserted, nil
}

func (s *Store) ComputeFinanceSummary(ctx context.Context, propertyID int64, month string) (*FinanceSummary, error) {
	summary := &FinanceSummary{Month: month, Breakdown: []FinanceCategoryBreakdown{}}

	_ = s.DB.QueryRowContext(ctx, `
		SELECT
			COALESCE(SUM(CASE WHEN direction = 'incoming' THEN amount_cents ELSE 0 END), 0),
			COALESCE(SUM(CASE WHEN direction = 'outgoing' THEN amount_cents ELSE 0 END), 0)
		FROM finance_transactions
		WHERE property_id = ?`, propertyID).
		Scan(&summary.TotalIncomingCents, &summary.TotalOutgoingCents)

	_ = s.DB.QueryRowContext(ctx, `
		SELECT
			COALESCE(SUM(CASE WHEN direction = 'incoming' THEN amount_cents ELSE 0 END), 0),
			COALESCE(SUM(CASE WHEN direction = 'outgoing' THEN amount_cents ELSE 0 END), 0)
		FROM finance_transactions
		WHERE property_id = ? AND substr(transaction_date, 1, 7) = ?`, propertyID, month).
		Scan(&summary.MonthlyIncomingCents, &summary.MonthlyOutgoingCents)
	summary.MonthlyNetCents = summary.MonthlyIncomingCents - summary.MonthlyOutgoingCents

	_ = s.DB.QueryRowContext(ctx, `
		SELECT COALESCE(SUM(ft.amount_cents), 0)
		FROM finance_transactions ft
		INNER JOIN finance_categories fc ON fc.id = ft.category_id
		WHERE ft.property_id = ?
		  AND ft.direction = 'incoming'
		  AND fc.counts_toward_property_income = 1`, propertyID).
		Scan(&summary.PropertyIncomeCents)

	_ = s.DB.QueryRowContext(ctx, `
		SELECT COALESCE(SUM(ft.amount_cents), 0)
		FROM finance_transactions ft
		INNER JOIN finance_categories fc ON fc.id = ft.category_id
		WHERE ft.property_id = ?
		  AND ft.direction = 'incoming'
		  AND fc.counts_toward_property_income = 1
		  AND substr(ft.transaction_date, 1, 7) = ?`, propertyID, month).
		Scan(&summary.MonthlyPropertyIncomeCents)

	_ = s.DB.QueryRowContext(ctx, `
		SELECT COALESCE(SUM(amount_cents), 0)
		FROM finance_transactions
		WHERE property_id = ?
		  AND source_type = 'cleaning_salary'
		  AND source_reference_id = ?`, propertyID, month).
		Scan(&summary.CleanerExpenseCents)

	if summary.MonthlyPropertyIncomeCents > 0 {
		summary.CleanerMargin = float64(summary.CleanerExpenseCents) / float64(summary.MonthlyPropertyIncomeCents)
	}

	rows, err := s.DB.QueryContext(ctx, `
		SELECT ft.category_id, fc.code, fc.title,
			COALESCE(SUM(CASE WHEN ft.direction = 'incoming' THEN ft.amount_cents ELSE 0 END), 0) AS incoming_cents,
			COALESCE(SUM(CASE WHEN ft.direction = 'outgoing' THEN ft.amount_cents ELSE 0 END), 0) AS outgoing_cents
		FROM finance_transactions ft
		LEFT JOIN finance_categories fc ON fc.id = ft.category_id
		WHERE ft.property_id = ? AND substr(ft.transaction_date, 1, 7) = ?
		GROUP BY ft.category_id, fc.code, fc.title
		ORDER BY outgoing_cents DESC, incoming_cents DESC`, propertyID, month)
	if err != nil {
		return summary, nil
	}
	defer rows.Close()
	for rows.Next() {
		var r FinanceCategoryBreakdown
		if err := rows.Scan(&r.CategoryID, &r.CategoryCode, &r.CategoryTitle, &r.IncomingCents, &r.OutgoingCents); err != nil {
			return summary, nil
		}
		summary.Breakdown = append(summary.Breakdown, r)
	}
	return summary, nil
}

func findFinanceCategoryByCodeTx(ctx context.Context, tx *sql.Tx, propertyID int64, code string) (int64, error) {
	var id int64
	err := tx.QueryRowContext(ctx, `
		SELECT id
		FROM finance_categories
		WHERE active = 1
		  AND code = ?
		  AND (property_id IS NULL OR property_id = ?)
		ORDER BY CASE WHEN property_id IS NULL THEN 1 ELSE 0 END
		LIMIT 1`, code, propertyID).Scan(&id)
	if err == sql.ErrNoRows {
		return 0, nil
	}
	return id, err
}

func parseMonthYYYYMM(month string) (int, int, error) {
	var y, m int
	if _, err := fmt.Sscanf(month, "%d-%d", &y, &m); err != nil {
		return 0, 0, fmt.Errorf("month must be YYYY-MM")
	}
	if y < 2000 || y > 3000 || m < 1 || m > 12 {
		return 0, 0, fmt.Errorf("month must be YYYY-MM")
	}
	return y, m, nil
}

func nullStringValue(v sql.NullString) interface{} {
	if !v.Valid {
		return nil
	}
	return v.String
}

func nullInt64Value(v sql.NullInt64) interface{} {
	if !v.Valid {
		return nil
	}
	return v.Int64
}

func scanFinanceTransactionsRows(rows *sql.Rows) ([]FinanceTransaction, error) {
	out := make([]FinanceTransaction, 0)
	for rows.Next() {
		var r FinanceTransaction
		var txDate, created, updated string
		var auto, income, mapped int
		if err := rows.Scan(&r.ID, &r.PropertyID, &txDate, &r.Direction, &r.AmountCents, &r.CategoryID,
			&r.Note, &r.SourceType, &r.SourceReference, &auto, &r.AttachmentPath, &created, &updated,
			&r.CategoryCode, &r.CategoryTitle, &income, &mapped); err != nil {
			return nil, err
		}
		r.TransactionDate, _ = time.Parse(time.RFC3339, txDate)
		r.CreatedAt, _ = time.Parse(time.RFC3339, created)
		r.UpdatedAt, _ = time.Parse(time.RFC3339, updated)
		r.IsAutoGenerated = auto == 1
		r.CountsTowardIncome = income == 1
		r.MappedToStay = mapped == 1
		out = append(out, r)
	}
	return out, rows.Err()
}
