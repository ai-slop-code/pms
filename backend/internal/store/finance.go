func (s *Store) ListFinanceCategories(ctx context.Context, propertyID int64) ([]FinanceCategory, error) {
	rows, err := s.DB.QueryContext(ctx, `
		SELECT id, property_id, code, title, direction, counts_toward_property_income, active, created_at, updated_at
		FROM finance_categories
		WHERE active = 1 AND (property_id IS NULL OR property_id = ?)
		ORDER BY CASE WHEN property_id IS NULL THEN 1 ELSE 0 END, title ASC`, propertyID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	out := make([]FinanceCategory, 0)
	for rows.Next() {
		var c FinanceCategory
		var income, active int
		var created, updated string
		if err := rows.Scan(&c.ID, &c.PropertyID, &c.Code, &c.Title, &c.Direction, &income, &active, &created, &updated); err != nil {
			return nil, err
		}
		c.CountsTowardPropertyIncome = income == 1
		c.Active = active == 1
		c.CreatedAt, _ = time.Parse(time.RFC3339, created)
		c.UpdatedAt, _ = time.Parse(time.RFC3339, updated)
		out = append(out, c)
	}
	return out, rows.Err()
}

