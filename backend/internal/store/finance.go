func (s *Store) UpdateFinanceTransaction(ctx context.Context, propertyID, id int64, txDate *time.Time, direction *string, amountCents *int, categoryID *int64, note *string, attachmentPath *string) (*FinanceTransaction, error) {
	row, err := s.GetFinanceTransactionByID(ctx, propertyID, id)
	if err != nil {
		return nil, err
	}
	if row.IsAutoGenerated {
		return nil, fmt.Errorf("auto_generated_transactions_are_read_only")
	}
	if txDate != nil {
		row.TransactionDate = txDate.UTC()
	}
	if direction != nil {
		row.Direction = *direction
	}
	if amountCents != nil {
		row.AmountCents = *amountCents
	}
	if categoryID != nil {
		row.CategoryID = sql.NullInt64{Int64: *categoryID, Valid: *categoryID > 0}
	}
	if note != nil {
		row.Note = sql.NullString{String: *note, Valid: *note != ""}
	}
	if attachmentPath != nil {
		row.AttachmentPath = sql.NullString{String: *attachmentPath, Valid: *attachmentPath != ""}
	}
	now := time.Now().UTC().Format(time.RFC3339)
	_, err = s.DB.ExecContext(ctx, `
		UPDATE finance_transactions
		SET transaction_date = ?, direction = ?, amount_cents = ?, category_id = ?, note = ?, attachment_path = ?, updated_at = ?
		WHERE id = ? AND property_id = ?`,
		row.TransactionDate.UTC().Format(time.RFC3339), row.Direction, row.AmountCents, nullInt64Value(row.CategoryID),
		nullStringValue(row.Note), nullStringValue(row.AttachmentPath), now, id, propertyID)
	if err != nil {
		return nil, err
	}
	return s.GetFinanceTransactionByID(ctx, propertyID, id)
}

