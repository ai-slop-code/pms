func (s *Server) postFinanceRecurringRule(w http.ResponseWriter, r *http.Request) {
	actor, pid, ok := s.requirePropertyModuleAccess(w, r, permissions.Finance, permissions.LevelWrite)
	if !ok {
		return
	}
	var body struct {
		Title         string  `json:"title"`
		CategoryID    *int64  `json:"category_id"`
		AmountCents   int     `json:"amount_cents"`
		Direction     string  `json:"direction"`
		StartMonth    string  `json:"start_month"`
		EndMonth      *string `json:"end_month"`
		EffectiveFrom string  `json:"effective_from"`
		EffectiveTo   *string `json:"effective_to"`
		Active        *bool   `json:"active"`
	}
	if err := ReadJSON(r, &body); err != nil {
		WriteError(w, http.StatusBadRequest, "invalid json")
		return
	}
	body.Title = strings.TrimSpace(body.Title)
	if body.Title == "" {
		WriteError(w, http.StatusBadRequest, "title is required")
		return
	}
	if body.AmountCents < 0 {
		WriteError(w, http.StatusBadRequest, "amount_cents must be >= 0")
		return
	}
	if body.Direction != "incoming" && body.Direction != "outgoing" {
		WriteError(w, http.StatusBadRequest, "direction must be incoming or outgoing")
		return
	}
	if _, _, err := parseFinanceMonth(strings.TrimSpace(body.StartMonth)); err != nil {
		WriteError(w, http.StatusBadRequest, "start_month must be YYYY-MM")
		return
	}
	if body.EndMonth != nil && strings.TrimSpace(*body.EndMonth) != "" {
		if _, _, err := parseFinanceMonth(strings.TrimSpace(*body.EndMonth)); err != nil {
			WriteError(w, http.StatusBadRequest, "end_month must be YYYY-MM")
			return
		}
	}
	effFrom, err := time.Parse(time.RFC3339, strings.TrimSpace(body.EffectiveFrom))
	if err != nil {
		WriteError(w, http.StatusBadRequest, "effective_from must be RFC3339")
		return
	}
	var effTo sql.NullTime
	if body.EffectiveTo != nil && strings.TrimSpace(*body.EffectiveTo) != "" {
		t, err := time.Parse(time.RFC3339, strings.TrimSpace(*body.EffectiveTo))
		if err != nil {
			WriteError(w, http.StatusBadRequest, "effective_to must be RFC3339")
			return
		}
		effTo = sql.NullTime{Time: t, Valid: true}
	}
	var cat sql.NullInt64
	if body.CategoryID != nil && *body.CategoryID > 0 {
		if _, err := s.Store.GetFinanceCategoryByID(r.Context(), pid, *body.CategoryID); err != nil {
			WriteError(w, http.StatusBadRequest, "invalid category_id")
			return
		}
		cat = sql.NullInt64{Int64: *body.CategoryID, Valid: true}
	}
	active := true
	if body.Active != nil {
		active = *body.Active
	}
	row, err := s.Store.CreateFinanceRecurringRule(r.Context(), &store.FinanceRecurringRule{
		PropertyID:    pid,
		Title:         body.Title,
		CategoryID:    cat,
		AmountCents:   body.AmountCents,
		Direction:     body.Direction,
		Frequency:     "monthly",
		StartMonth:    strings.TrimSpace(body.StartMonth),
		EndMonth:      sql.NullString{String: strings.TrimSpace(ptrString(body.EndMonth)), Valid: body.EndMonth != nil && strings.TrimSpace(*body.EndMonth) != ""},
		EffectiveFrom: effFrom,
		EffectiveTo:   effTo,
		Active:        active,
	})
	if err != nil {
		WriteError(w, http.StatusInternalServerError, "create failed")
		return
	}
	s.audit(r, actor, "finance_recurring_rule_create", "finance_recurring_rule", strconv.FormatInt(row.ID, 10), "success")
	WriteJSON(w, http.StatusCreated, map[string]interface{}{"rule": recurringToRow(*row)})
}

