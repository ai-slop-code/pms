func (s *Server) importFinanceBookingPayouts(w http.ResponseWriter, r *http.Request) {
	actor, pid, ok := s.requirePropertyModuleAccess(w, r, permissions.Finance, permissions.LevelWrite)
	if !ok {
		return
	}
	prop, err := s.Store.GetProperty(r.Context(), pid)
	if err != nil {
		WriteError(w, http.StatusNotFound, "property not found")
		return
	}
	loc, err := time.LoadLocation(prop.Timezone)
	if err != nil {
		loc = time.UTC
	}
	r.Body = http.MaxBytesReader(w, r.Body, (25<<20)+(1<<20))
	if err := r.ParseMultipartForm(25 << 20); err != nil {
		WriteError(w, http.StatusBadRequest, "invalid multipart form")
		return
	}
	file, _, err := r.FormFile("file")
	if err != nil {
		WriteError(w, http.StatusBadRequest, "missing file")
		return
	}
	defer file.Close()
	rows, warnings, err := parseBookingPayoutCSV(file, loc)
	if err != nil {
		WriteError(w, http.StatusBadRequest, err.Error())
		return
	}
	bookingIncomeCategoryID, err := s.Store.FinanceCategoryIDByCode(r.Context(), pid, "booking_income")
	if err != nil || bookingIncomeCategoryID == 0 {
		WriteError(w, http.StatusBadRequest, "booking_income category missing")
		return
	}
	resp := bookingPayoutImportResponse{OK: true, Warnings: warnings}
	for _, row := range rows {
		existing, err := s.Store.GetBookingPayoutByReference(r.Context(), pid, row.ReferenceNumber)
		if err == nil && existing != nil {
			resp.Duplicates++
			if !existing.OccupancyID.Valid {
				occ, matchErr := s.resolveBookingPayoutOccupancy(
					r.Context(),
					pid,
					row.ReferenceNumber,
					row.CheckInDate,
					row.CheckOutDate,
					row.GuestName,
					loc,
				)
				if matchErr != nil {
					resp.Failed++
					continue
				}
				if occ != nil {
					occID := occ.ID
					_ = s.Store.UpdateBookingPayoutMapping(r.Context(), pid, row.ReferenceNumber, &occID)
					resp.Mapped++
				}
			}
			continue
		}
		if err != nil && err != sql.ErrNoRows {
			resp.Failed++
			continue
		}
		var existingTxID int64
		if tx, err := s.Store.FinanceTransactionBySourceReference(r.Context(), pid, "booking_payout", row.ReferenceNumber); err == nil && tx != nil {
			existingTxID = tx.ID
		}
		var occupancyID sql.NullInt64
		occ, matchErr := s.resolveBookingPayoutOccupancy(
			r.Context(),
			pid,
			row.ReferenceNumber,
			row.CheckInDate,
			row.CheckOutDate,
			row.GuestName,
			loc,
		)
		if matchErr != nil {
			resp.Failed++
			continue
		}
		if occ != nil {
			occupancyID = sql.NullInt64{Int64: occ.ID, Valid: true}
		}
		txInput := &store.FinanceTransaction{
			PropertyID:      pid,
			TransactionDate: row.PayoutDate,
			Direction:       "incoming",
			AmountCents:     row.NetCents,
			CategoryID:      sql.NullInt64{Int64: bookingIncomeCategoryID, Valid: true},
			Note:            sql.NullString{String: "Booking.com payout " + row.ReferenceNumber, Valid: true},
			SourceType:      "booking_payout",
			SourceReference: sql.NullString{String: row.ReferenceNumber, Valid: true},
			IsAutoGenerated: true,
		}
		payout := &store.FinanceBookingPayout{
			PropertyID:             pid,
			ReferenceNumber:        row.ReferenceNumber,
			PayoutID:               sql.NullString{String: row.PayoutID, Valid: row.PayoutID != ""},
			RowType:                sql.NullString{String: row.RowType, Valid: row.RowType != ""},
			CheckInDate:            sql.NullString{String: row.CheckInDate, Valid: row.CheckInDate != ""},
			CheckOutDate:           sql.NullString{String: row.CheckOutDate, Valid: row.CheckOutDate != ""},
			GuestName:              sql.NullString{String: row.GuestName, Valid: row.GuestName != ""},
			ReservationStatus:      sql.NullString{String: row.ReservationStatus, Valid: row.ReservationStatus != ""},
			Currency:               sql.NullString{String: row.Currency, Valid: row.Currency != ""},
			PaymentStatus:          sql.NullString{String: row.PaymentStatus, Valid: row.PaymentStatus != ""},
			AmountCents:            sql.NullInt64{Int64: int64(row.AmountCents), Valid: true},
			CommissionCents:        sql.NullInt64{Int64: int64(row.CommissionCents), Valid: true},
			PaymentServiceFeeCents: sql.NullInt64{Int64: int64(row.PaymentFeeCents), Valid: true},
			NetCents:               row.NetCents,
			PayoutDate:             row.PayoutDate,
			OccupancyID:            occupancyID,
			RawRowJSON:             sql.NullString{String: row.RawJSON, Valid: row.RawJSON != ""},
		}
		if _, err := s.Store.ImportBookingPayoutRow(r.Context(), txInput, payout, existingTxID); err != nil {
			resp.Failed++
			continue
		}
		if occupancyID.Valid {
			resp.Mapped++
		}
		resp.Imported++
	}
	s.audit(r, actor, "finance_booking_payout_import", "property", strconv.FormatInt(pid, 10), "success")
	WriteJSON(w, http.StatusOK, resp)
}

func (s *Server) rematchFinanceBookingPayouts(w http.ResponseWriter, r *http.Request) {
	actor, pid, ok := s.requirePropertyModuleAccess(w, r, permissions.Finance, permissions.LevelWrite)
	if !ok {
		return
	}
	prop, err := s.Store.GetProperty(r.Context(), pid)
	if err != nil {
		WriteError(w, http.StatusNotFound, "property not found")
		return
	}
	loc, err := time.LoadLocation(prop.Timezone)
	if err != nil {
		loc = time.UTC
	}
	month := strings.TrimSpace(r.URL.Query().Get("month"))
	if month != "" {
		if _, _, err := parseFinanceMonth(month); err != nil {
			WriteError(w, http.StatusBadRequest, "month must be YYYY-MM")
			return
		}
	}
	onlyUnmapped := true
	onlyUnmappedRaw := strings.TrimSpace(strings.ToLower(r.URL.Query().Get("only_unmapped")))
	if onlyUnmappedRaw == "0" || onlyUnmappedRaw == "false" || onlyUnmappedRaw == "no" {
		onlyUnmapped = false
	}
	rows, err := s.Store.ListBookingPayouts(r.Context(), pid, month, nil)
	if err != nil {
		WriteError(w, http.StatusInternalServerError, "database error")
		return
	}
	resp := bookingPayoutRematchResponse{OK: true}
	for _, row := range rows {
		resp.Scanned++
		if onlyUnmapped && row.OccupancyID.Valid {
			resp.AlreadyMapped++
			continue
		}
		occ, err := s.resolveBookingPayoutOccupancy(
			r.Context(),
			pid,
			row.ReferenceNumber,
			strings.TrimSpace(row.CheckInDate.String),
			strings.TrimSpace(row.CheckOutDate.String),
			fixCSVMojibake(strings.TrimSpace(row.GuestName.String)),
			loc,
		)
		if err != nil {
			resp.Failed++
			continue
		}
		if occ == nil {
			continue
		}
		resp.Matched++
		occID := occ.ID
		if err := s.Store.UpdateBookingPayoutMapping(r.Context(), pid, row.ReferenceNumber, &occID); err != nil {
			resp.Failed++
			continue
		}
		resp.Updated++
	}
	s.audit(r, actor, "finance_booking_payout_rematch", "property", strconv.FormatInt(pid, 10), "success")
	WriteJSON(w, http.StatusOK, resp)
}

