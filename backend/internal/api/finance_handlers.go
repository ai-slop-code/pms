func (s *Server) postFinanceTransaction(w http.ResponseWriter, r *http.Request) {
	actor, pid, ok := s.requirePropertyModuleAccess(w, r, permissions.Finance, permissions.LevelWrite)
	if !ok {
		return
	}
	if s.AttachmentUploadLimiter != nil {
		if !s.AttachmentUploadLimiter.Allow(fmt.Sprintf("attachment_upload:%d", actor.ID)) {
			w.Header().Set("Retry-After", "2")
			WriteError(w, http.StatusTooManyRequests, "too many attachment uploads")
			return
		}
	}
	prop, err := s.Store.GetProperty(r.Context(), pid)
	if err != nil {
		WriteError(w, http.StatusNotFound, "property not found")
		return
	}
	loc, err := time.LoadLocation(prop.Timezone)
	if err != nil {
		loc = time.UTC
	}
	row, pending, err := s.parseTransactionCreateRequest(r, pid, loc)
	if err != nil {
		WriteError(w, http.StatusBadRequest, err.Error())
		return
	}
	if pending != nil {
		defer pending.Close()
	}
	created, err := s.Store.CreateFinanceTransaction(r.Context(), row)
	if err != nil {
		WriteError(w, http.StatusInternalServerError, "create failed")
		return
	}
	if pending != nil {
		path, saveErr := s.saveFinanceAttachment(pid, created.ID, pending.header, pending.file)
		if saveErr != nil {
			WriteError(w, http.StatusBadRequest, saveErr.Error())
			return
		}
		if path != "" {
			updated, updErr := s.Store.UpdateFinanceTransaction(r.Context(), pid, created.ID, nil, nil, nil, nil, nil, &path)
			if updErr != nil {
				WriteError(w, http.StatusInternalServerError, "attachment link failed")
				return
			}
			created = updated
		}
	}
	s.audit(r, actor, "finance_transaction_create", "finance_transaction", strconv.FormatInt(created.ID, 10), "success")
	WriteJSON(w, http.StatusCreated, map[string]interface{}{"transaction": txToRow(*created)})
}

func (s *Server) patchFinanceTransaction(w http.ResponseWriter, r *http.Request) {
	actor, pid, ok := s.requirePropertyModuleAccess(w, r, permissions.Finance, permissions.LevelWrite)
	if !ok {
		return
	}
	tid, err := strconv.ParseInt(chi.URLParam(r, "transactionId"), 10, 64)
	if err != nil {
		WriteError(w, http.StatusBadRequest, "invalid transaction id")
		return
	}
	prop, err := s.Store.GetProperty(r.Context(), pid)
	if err != nil {
		WriteError(w, http.StatusNotFound, "property not found")
		return
	}
	loc, err := time.LoadLocation(prop.Timezone)
	if err != nil {
		loc = time.UTC
	}
	var body struct {
		TransactionDate *string `json:"transaction_date"`
		Direction       *string `json:"direction"`
		AmountCents     *int    `json:"amount_cents"`
		CategoryID      *int64  `json:"category_id"`
		Note            *string `json:"note"`
	}
	if err := ReadJSON(r, &body); err != nil {
		WriteError(w, http.StatusBadRequest, "invalid json")
		return
	}
	var txDate *time.Time
	if body.TransactionDate != nil {
		t, err := parseFinanceDate(strings.TrimSpace(*body.TransactionDate), loc)
		if err != nil {
			WriteError(w, http.StatusBadRequest, "transaction_date must be RFC3339 or YYYY-MM-DD")
			return
		}
		txDate = &t
	}
	if body.Direction != nil {
		d := strings.TrimSpace(*body.Direction)
		if d != "incoming" && d != "outgoing" {
			WriteError(w, http.StatusBadRequest, "direction must be incoming or outgoing")
			return
		}
		body.Direction = &d
	}
	if body.AmountCents != nil && *body.AmountCents < 0 {
		WriteError(w, http.StatusBadRequest, "amount_cents must be >= 0")
		return
	}
	if body.CategoryID != nil && *body.CategoryID > 0 {
		if _, err := s.Store.GetFinanceCategoryByID(r.Context(), pid, *body.CategoryID); err != nil {
			WriteError(w, http.StatusBadRequest, "invalid category_id")
			return
		}
	}
	updated, err := s.Store.UpdateFinanceTransaction(r.Context(), pid, tid, txDate, body.Direction, body.AmountCents, body.CategoryID, body.Note, nil)
	if err != nil {
		if strings.Contains(err.Error(), "read_only") {
			WriteError(w, http.StatusBadRequest, "auto-generated transactions cannot be edited")
			return
		}
		if errors.Is(err, sql.ErrNoRows) {
			WriteError(w, http.StatusNotFound, "transaction not found")
			return
		}
		WriteError(w, http.StatusInternalServerError, "update failed")
		return
	}
	s.audit(r, actor, "finance_transaction_update", "finance_transaction", strconv.FormatInt(updated.ID, 10), "success")
	WriteJSON(w, http.StatusOK, map[string]interface{}{"transaction": txToRow(*updated)})
}

