func parseBookingPayoutCSV(in io.Reader, loc *time.Location) ([]bookingPayoutCSVRow, []string, error) {
	r := csv.NewReader(in)
	r.FieldsPerRecord = -1
	r.TrimLeadingSpace = true
	header, err := r.Read()
	if err != nil {
		return nil, nil, fmt.Errorf("invalid csv header")
	}
	idx := map[string]int{}
	for i, h := range header {
		key := strings.ToLower(strings.TrimSpace(strings.TrimPrefix(h, "\ufeff")))
		idx[key] = i
	}
	if _, ok := idx["reference number"]; !ok {
		if i, aliasOK := idx["refference number"]; aliasOK {
			idx["reference number"] = i
		}
	}
	required := []string{"reference number", "net", "payout date"}
	for _, k := range required {
		if _, ok := idx[k]; !ok {
			return nil, nil, fmt.Errorf("csv missing required column: %s", k)
		}
	}
	warnings := []string{}
	out := make([]bookingPayoutCSVRow, 0)
	line := 1
	for {
		rec, err := r.Read()
		if err == io.EOF {
			break
		}
		line++
		if err != nil {
			warnings = append(warnings, fmt.Sprintf("line %d skipped: %v", line, err))
			continue
		}
		ref := csvVal(rec, idx, "reference number")
		if ref == "" {
			warnings = append(warnings, fmt.Sprintf("line %d skipped: missing reference number", line))
			continue
		}
		net, err := parseMoneyToCents(csvVal(rec, idx, "net"))
		if err != nil {
			warnings = append(warnings, fmt.Sprintf("line %d skipped: invalid net", line))
			continue
		}
		payoutDate, err := parseBookingDate(csvVal(rec, idx, "payout date"), loc)
		if err != nil {
			warnings = append(warnings, fmt.Sprintf("line %d skipped: invalid payout date", line))
			continue
		}
		amount, _ := parseMoneyToCents(csvVal(rec, idx, "amount"))
		commission, _ := parseMoneyToCents(csvVal(rec, idx, "commission"))
		payFee, _ := parseMoneyToCents(csvVal(rec, idx, "payments service fee"))
		checkIn, _ := parseBookingDateOnly(csvVal(rec, idx, "check-in"), loc)
		checkOut, _ := parseBookingDateOnly(csvVal(rec, idx, "checkout"), loc)
		rawMap := map[string]string{}
		for k, i := range idx {
			if i >= 0 && i < len(rec) {
				rawMap[k] = fixCSVMojibake(rec[i])
			}
		}
		rawJSON, _ := json.Marshal(rawMap)
		out = append(out, bookingPayoutCSVRow{
			ReferenceNumber:   ref,
			RowType:           csvVal(rec, idx, "type"),
			CheckInDate:       checkIn,
			CheckOutDate:      checkOut,
			GuestName:         fixCSVMojibake(csvVal(rec, idx, "guest name")),
			ReservationStatus: csvVal(rec, idx, "reservation status"),
			Currency:          csvVal(rec, idx, "currency"),
			PaymentStatus:     csvVal(rec, idx, "payment status"),
			AmountCents:       amount,
			CommissionCents:   commission,
			PaymentFeeCents:   payFee,
			NetCents:          net,
			PayoutDate:        payoutDate,
			PayoutID:          csvVal(rec, idx, "payout id"),
			RawJSON:           string(rawJSON),
		})
	}
	return out, warnings, nil
}

func csvVal(rec []string, idx map[string]int, key string) string {
	i, ok := idx[key]
	if !ok || i < 0 || i >= len(rec) {
		return ""
	}
	return strings.TrimSpace(rec[i])
}

func parseMoneyToCents(v string) (int, error) {
	s := strings.TrimSpace(v)
	if s == "" {
		return 0, nil
	}
	s = strings.ReplaceAll(s, " ", "")
	s = strings.ReplaceAll(s, ",", ".")
	f, err := strconv.ParseFloat(s, 64)
	if err != nil {
		return 0, err
	}
	return int(math.Round(f * 100)), nil
}

func parseBookingDate(v string, loc *time.Location) (time.Time, error) {
	t, err := time.ParseInLocation("2 Jan 2006", strings.TrimSpace(v), loc)
	if err != nil {
		return time.Time{}, err
	}
	return time.Date(t.Year(), t.Month(), t.Day(), 12, 0, 0, 0, loc).UTC(), nil
}

func parseBookingDateOnly(v string, loc *time.Location) (string, error) {
	t, err := time.ParseInLocation("2 Jan 2006", strings.TrimSpace(v), loc)
	if err != nil {
		return "", err
	}
	return t.Format("2006-01-02"), nil
}

func (s *Server) parseTransactionCreateRequest(r *http.Request, propertyID int64, loc *time.Location) (*store.FinanceTransaction, *pendingAttachment, error) {
	ct := strings.ToLower(strings.TrimSpace(r.Header.Get("Content-Type")))
	var dateRaw, direction string
	var amountCents int
	var categoryID int64
	var note string
	var pending *pendingAttachment

	if strings.HasPrefix(ct, "multipart/form-data") {
		r.Body = http.MaxBytesReader(nil, r.Body, (20<<20)+(1<<20))
		if err := r.ParseMultipartForm(20 << 20); err != nil {
			return nil, nil, fmt.Errorf("invalid multipart body")
		}
		dateRaw = strings.TrimSpace(r.FormValue("transaction_date"))
		direction = strings.TrimSpace(r.FormValue("direction"))
		note = strings.TrimSpace(r.FormValue("note"))
		amountRaw := strings.TrimSpace(r.FormValue("amount_cents"))
		if amountRaw == "" {
			amountRaw = strings.TrimSpace(r.FormValue("amount"))
		}
		if amountRaw == "" {
			return nil, nil, fmt.Errorf("amount_cents is required")
		}
		v, err := strconv.Atoi(amountRaw)
		if err != nil {
			return nil, nil, fmt.Errorf("amount_cents must be a number")
		}
		amountCents = v
		if catRaw := strings.TrimSpace(r.FormValue("category_id")); catRaw != "" {
			cid, err := strconv.ParseInt(catRaw, 10, 64)
			if err != nil {
				return nil, nil, fmt.Errorf("category_id must be a number")
			}
			categoryID = cid
		}
		file, header, err := r.FormFile("attachment")
		if err == nil {
			pending = &pendingAttachment{file: file, header: header}
		}
	} else {
		var body struct {
			TransactionDate string `json:"transaction_date"`
			Direction       string `json:"direction"`
			AmountCents     int    `json:"amount_cents"`
			CategoryID      *int64 `json:"category_id"`
			Note            string `json:"note"`
			// attachment_path is intentionally ignored: attachments must be
			// uploaded via multipart so the server controls their storage path.
		}
		if err := ReadJSON(r, &body); err != nil {
			return nil, nil, fmt.Errorf("invalid json")
		}
		dateRaw = strings.TrimSpace(body.TransactionDate)
		direction = strings.TrimSpace(body.Direction)
		amountCents = body.AmountCents
		note = strings.TrimSpace(body.Note)
		if body.CategoryID != nil {
			categoryID = *body.CategoryID
		}
	}
	if dateRaw == "" {
		return nil, nil, fmt.Errorf("transaction_date is required")
	}
	txDate, err := parseFinanceDate(dateRaw, loc)
	if err != nil {
		return nil, nil, fmt.Errorf("transaction_date must be RFC3339 or YYYY-MM-DD")
	}
	if direction != "incoming" && direction != "outgoing" {
		return nil, nil, fmt.Errorf("direction must be incoming or outgoing")
	}
	if amountCents < 0 {
		return nil, nil, fmt.Errorf("amount_cents must be >= 0")
	}
	if categoryID > 0 {
		if _, err := s.Store.GetFinanceCategoryByID(r.Context(), propertyID, categoryID); err != nil {
			return nil, nil, fmt.Errorf("invalid category_id")
		}
	}
	return &store.FinanceTransaction{
		PropertyID:      propertyID,
		TransactionDate: txDate,
		Direction:       direction,
		AmountCents:     amountCents,
		CategoryID:      sql.NullInt64{Int64: categoryID, Valid: categoryID > 0},
		Note:            sql.NullString{String: note, Valid: note != ""},
		SourceType:      "manual",
		IsAutoGenerated: false,
	}, pending, nil
}

type pendingAttachment struct {
	file   multipart.File
	header *multipart.FileHeader
}

func (p *pendingAttachment) Close() {
	if p != nil && p.file != nil {
		_ = p.file.Close()
	}
}

func parseFinanceDate(raw string, loc *time.Location) (time.Time, error) {
	raw = strings.TrimSpace(raw)
	if t, err := time.Parse(time.RFC3339, raw); err == nil {
		return t.UTC(), nil
	}
	if t, err := time.ParseInLocation("2006-01-02", raw, loc); err == nil {
		// Use local noon to avoid month drift in positive-offset timezones.
		return time.Date(t.Year(), t.Month(), t.Day(), 12, 0, 0, 0, loc).UTC(), nil
	}
	return time.Time{}, fmt.Errorf("invalid date")
}

func (s *Server) saveFinanceAttachment(propertyID, transactionID int64, header *multipart.FileHeader, file multipart.File) (string, error) {
	if header == nil || file == nil {
		return "", nil
	}
	if transactionID <= 0 {
		return "", fmt.Errorf("invalid transaction id")
	}
	base := filepath.Base(header.Filename)
	base = strings.ReplaceAll(base, " ", "_")
	if base == "." || base == "" || base == "/" || strings.Contains(base, "..") {
		return "", fmt.Errorf("invalid attachment filename")
	}
	baseDir := strings.TrimSpace(s.DataDir)
	if baseDir == "" {
		baseDir = "./data"
	}
	dir := filepath.Join(baseDir, "attachments", fmt.Sprintf("%d", propertyID), fmt.Sprintf("%d", transactionID))
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return "", fmt.Errorf("unable to create attachment directory")
	}
	fullPath := filepath.Join(dir, base)
	dst, err := os.Create(fullPath)
	if err != nil {
		return "", fmt.Errorf("unable to create attachment file")
	}
	defer dst.Close()
	if _, err := io.Copy(dst, file); err != nil {
		return "", fmt.Errorf("unable to save attachment")
	}
	return filepath.ToSlash(filepath.Join("attachments", fmt.Sprintf("%d", propertyID), fmt.Sprintf("%d", transactionID), base)), nil
}

func ptrString(v *string) string {
	if v == nil {
		return ""
	}
	return *v
}

func parseFinanceMonth(month string) (int, int, error) {
	var y, m int
	if _, err := fmt.Sscanf(month, "%d-%d", &y, &m); err != nil {
		return 0, 0, fmt.Errorf("month must be YYYY-MM")
	}
	if y < 2000 || y > 3000 || m < 1 || m > 12 {
		return 0, 0, fmt.Errorf("month must be YYYY-MM")
	}
	return y, m, nil
}
